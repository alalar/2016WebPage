function translateHTMLElementsTo($element,strProperty) {
	if ($element.length>0)
		$element.each( function(index){
			$(this).html(objTranslate[strProperty][index]);
			});
}

function translateTextElementsTo($element,strProperty) {
	if ($element.length>0)
		$element.each( function(index){
			translateFirstChildren($(this),objTranslate[strProperty][index]);
			});
}

function translateFirstChildren($element,txtInfo) {
	if ($element.children().length>0)
		translateFirstChildren($($element.children()[0]),txtInfo)
	else
		$element.text($("<div>").html(txtInfo).text());
}